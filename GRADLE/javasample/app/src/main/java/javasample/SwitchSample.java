package javasample;

public class SwitchSample {
    public static void main(String[] args) {
        System.out.println("1-5評価");
        int eval = 1;
        switch (eval) {
            case 4:
                System.out.println("評価はとても悪いです。");
                break;
            default:
                System.out.println("aaaa");
                break;
        }
    }
}
